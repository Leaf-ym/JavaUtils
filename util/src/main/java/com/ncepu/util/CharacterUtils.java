package com.ncepu.util;

/**
 * @author wengym
 * @version 1.0
 * @desc 字符工具类
 * @date 2023/1/11 9:26
 */
public class CharacterUtils {
    /**
     * 十进制的ASCII码转字符
     *
     * @param code
     *
     * @author wengym
     *
     * @date 2023/1/11 9:28
     *
     * @return char
     */
    public static char toChar(int code) {
        char c = (char)code;
        return c;
    }

    /**
     * 字符转十进制的ASCII码
     *
     * @param c
     *
     * @author wengym
     *
     * @date 2023/1/11 9:31
     *
     * @return int
     */
    public static int toAscii(char c) {
        int ascii = c;
        return ascii;
    }
}
